use cairn_ssh::hostkey::sha256_fingerprint;

#[test]
fn fingerprint_ed25519_known_vector() {
    let pubkey_blob: &[u8] = &[
        0x00, 0x00, 0x00, 0x0b, b's', b's', b'h', b'-', b'e', b'd', b'2', b'5', b'5', b'1', b'9',
        0x00, 0x00, 0x00, 0x20, 0x45, 0x65, 0xc5, 0x9a, 0x8c, 0x5f, 0x61, 0x25, 0x8a, 0x2b, 0xfa,
        0xd6, 0xcd, 0x9e, 0x8c, 0x51, 0x14, 0x9e, 0x5f, 0x3d, 0x76, 0x2d, 0x64, 0x15, 0x05, 0x01,
        0xa3, 0xfc, 0x88, 0x3c, 0x42, 0x20,
    ];
    let fp = sha256_fingerprint(pubkey_blob);
    assert!(fp.starts_with("SHA256:"));
    assert_eq!(fp.len(), "SHA256:".len() + 43);
}
